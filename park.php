                    <div class="col-xs-12 col-md-7">
                        <p>
                            Nacionalni park "Krka" smješten je na području Šibensko-kninske županije i obuhvaća površinu od 109 km² najljepšeg toka rijeke Krke i donji tok rijeke Čikole.<br/><br/>

                            Nacionalni park je prostrano, pretežito neizmijenjeno područje iznimnih i višestrukih prirodnih vrijednosti, a obuhvaća jedan ili više sačuvanih ili neznatno izmijenjenih ekosustava. Namjena mu je prije svega znanstvena, kulturna, odgojno-obrazovna i rekreativna, a zbog posjetitelja postoje i turističke djelatnosti.<br/><br/>

                            S potopljenim dijelom ušća Krka je duga oko 72,5 km i po dužini je 22. rijeka u Hrvatskoj. Izvire u podnožju planine Dinare, 3,5 km sjeveroistočno od Knina. Sa sedam sedrenih slapova i ukupnim padom od 224 m, Krka je prirodni i krški fenomen. Sedreni slapovi rijeke Krke temeljni su fenomen ove rijeke.
