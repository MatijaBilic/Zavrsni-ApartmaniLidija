                    <h4>Park prirode Telašćica</h4>
                    <div class="col-xs-12 col-md-7">
                        <p>
                            Uvala Telašćica smještena je u središnjem dijelu istočne obale Jadranskog mora, u jugoistočnom dijelu otoka Dugi otok. Zahvaljujući svojoj iznimnoj ljepoti, bogatstvu i značaju, ova uvala, okružena s 13 otoka i otočića, a sadrži i šest otočića unutar uvale, proglašena je Parkom prirode 1988. Status zaštićenog područja dobila je još 1980. zahvaljujući svojem iznimno vrijednom biljnom i životinjskom svijetu, geološkim i geomorfološkim fenomenima, vrijednim zajednicama morskog dna te zanimljivom arheološkom nasljeđu.<br/><br/>

                            Područje Parka prirode Telašćica je područje bogato suprotnostima, tihim i mirnim plažama i položenom obalom s jedne strane i divljim strmim klifovima s druge strane, područje šuma alepskog bora i hrasta crnike s jedne strane, te ogoljelog kamenjara s druge, područje obrađenih polja prekrivenih vinogradima i maslinicima, ali i područje degradiranih oblika vegetacije koja prekriva suha staništa.<br/><br/>
                            
                            Tri temeljna fenomena predstavljaju osnovna obilježja ovog područja: jedinstvena uvala Telašćica kao najsigurnija, najljepša i najveća prirodna luka u Jadranskom moru, u kojoj su 25 malih plaža, strmci otoka Dugi otok ili takozvane “stene“, koje se uzdižu do 161 metar nad morem i spuštaju se u dubinu do 90 m, i konačno slano jezero “Mir” s ljekovitim svojstvima.
                        </p>
