                    <div class="col-xs-12 col-md-7">
                        <p>
                            Putujući Jadranskom magistralom prema sjevernoj Dalmaciji, između dva poviješću bogata grada Šibenika i Zadra ukazat će vam se prirodni fenomen blizine dva jednako lijepa, a opet, toliko različita plavetnila. S jedne strane razvedena jadranska obala, poznata po svojoj primamljivoj ljepoti i čistoći, a sa druge, najveće prirodno jezero u Hrvatskoj. <br/><br/>
                            
