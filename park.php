                    <h4>Nacionalni park Kornati</h4>
                    <div class="col-xs-12 col-md-7">
                        <p>
                            Dio Kornatskog otočja, otprilike dvije trećine kopna i akvatorija, danas je nacionalni park. Iz Zakona o uvođenju najvećeg stupnja zaštite izostavljeni su Žut i Sita sa svojim otocima, iako geomorfološki, geografski i kulturološki pripadaju kornatskoj otočnoj skupini. <br/><br/>
                            
                            Od proglašenja otočja nacionalnim parkom, 1980., na Kornatima su provedena mnoga znanstvena istraživanja, osobito ona koja se odnose na prirodnu baštinu. Ova su istraživanja iznijela na vidjelo brojne obavijesti o geološkim, meteorološkim, zemljopisnim, biološkim i drugim osobitostima otočja. Popisano je do sada 537 biljnih vrsta na kopnu, 226 svojti bentoskih alga, oko 850 vrsta živih organizama u moru, a među njima 579 beskralježnjaka, 160 vrsta ribe, jedna kornjača i tri vrste sisavaca, od kojih je jedan - morska medvjedica - vjerojatno zauvijek izgubljen. <br/><br/>

                            Ukoliko poželite posjetiti Kornate izletničkim brodom na izboru Vam je cijela paleta mogućnosti tipa i kapaciteta izletničkih brodova. Jednodnevne posjete NP "Kornati" organiziraju i obavljaju brodari ili organizatori registrirani za djelatnost prijevoza putnika brodom i koji s Javnom ustanovom "Nacionalni park Kornati" sklope ugovor za obavljanje takve djelatnosti u NP "Kornati".
